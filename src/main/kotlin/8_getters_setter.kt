        These are the implicit getters and setters that are automatically generated by kotlin,
        but you can override them
        get() {
            return field
        }

        set(){
            field = value
        }
        */
        get(){
            return "First Name: $field."
        }
        set(value){
            println("$value was assigned to firstname property.")
            field = value
        }
}

fun main(){
    var user = User("Alex", "smith", 24)

    user.firstName = "John"

    // observe the output
    println(user.firstName)
    println(user.lastName)
    println(user.age)
}


