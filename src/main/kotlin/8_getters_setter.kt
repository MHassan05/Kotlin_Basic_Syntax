
/*
Encapsulation means to protect, to hide inner working of class to be accessed from outside. For that purpose,
we use getters and setters. Getters and Setters are implicit, they automatically generated, but you can override
them.
 */

class User(firstName: String = "First Name", var lastName: String = "Last Name", var age: Int = 0) {

